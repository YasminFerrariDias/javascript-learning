// Demonstra adição e remoção de elementos no início e fim de um array com push, pop, shift e unshift.
let users = []

// Adiciona no final da lista
users.push("Rodrigo")
users.push("João")
users.push("Marcos")

// Adiciona no inicio da lista
users.unshift("Ana")

// Remove do inicio do Array
users.shift()

// Remove do final do Array
users.pop()

console.log(users)
