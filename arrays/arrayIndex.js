// Demonstra como acessar itens de um array por índice, obter o tamanho e acessar o último elemento dinamicamente.
let fruits = ['Apple', "Banana", "Orange"]
console.log(fruits)

// Quantidade de itens do array
console.log(fruits.length)

// Acessa o item pelo índice
console.log(fruits[2])

// Acessa o item pelo índice que não existe 
console.log(fruits[109])

// Obtem dinamicamente a ultima posição 
console.log(fruits[fruits.length - 1])
